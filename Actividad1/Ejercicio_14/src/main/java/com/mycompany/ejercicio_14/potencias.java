public class potencias {
    //Metodo para hallar el cuadrado del numero
    public static double cuadrado (double numero){
        double cuadrado_numero;
        cuadrado_numero = Math.pow(numero,2);
        return cuadrado_numero;
    }
    //Metodo para hallar el cubo del numero 
    public static double cubo (double numero){
        double cubo_numero;
        cubo_numero = Math.pow (numero,3);
        return cubo_numero;
    }
}
